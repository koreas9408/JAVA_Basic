public class Operators {
    public void calculate() {
        System.out.println(1 + 2);
        System.out.println(3 - 1);
        System.out.println(5 * 4);
        System.out.println(6 / 2);

        // 몫을 구하는 연산자.
        System.out.println(5 / 2);

        // 나머지를 구하는 연산자.
        System.out.println(5 % 2);

    }
}
